#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	long	nb;

	nb = n;
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb = -nb;
	}
	c = '0' + nb % 10;
	nb = nb / 10;
	if (nb > 0)
	{
		ft_putnbr_fd(nb, fd);
	}
	write(fd, &c, 1);
}
