int	ft_str_is_numeric(char *str)
{
	int	a;

	a = 0;
	while (*str)
	{
		if (*str < '0' || *str > '9')
		{
			a++;
		}
		str++;
	}
	if (a > 0)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
