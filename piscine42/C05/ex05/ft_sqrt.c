int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb == 1)
		return (nb);
	if (nb <= 3)
		return (0);
	while (i <= nb / 2)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}

